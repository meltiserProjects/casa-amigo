package com.realestate.bot.model.enums;

/**
 * Статусы поисковых запросов
 */
public enum SearchStatus {
    /**
     * Активный поиск - бот проверяет новые объявления
     */
    ACTIVE,

    /**
     * Приостановленный поиск - бот не проверяет, но поиск сохранён
     */
    PAUSED,

    /**
     * Удалённый поиск - помечен для удаления
     */
    DELETED
}
