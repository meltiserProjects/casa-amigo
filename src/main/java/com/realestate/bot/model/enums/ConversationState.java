package com.realestate.bot.model.enums;

/**
 * Состояния диалога с пользователем при создании/редактировании поиска
 */
public enum ConversationState {
    /**
     * Ожидание ввода минимальной цены
     */
    WAITING_MIN_PRICE,

    /**
     * Ожидание ввода максимальной цены
     */
    WAITING_MAX_PRICE,

    /**
     * Ожидание выбора количества комнат
     */
    WAITING_NUM_ROOMS,

    /**
     * Ожидание выбора районов
     */
    WAITING_DISTRICTS,

    /**
     * Редактирование: ожидание минимальной цены
     */
    EDITING_MIN_PRICE,

    /**
     * Редактирование: ожидание максимальной цены
     */
    EDITING_MAX_PRICE,

    /**
     * Редактирование: ожидание выбора комнат
     */
    EDITING_NUM_ROOMS,

    /**
     * Редактирование: ожидание выбора районов
     */
    EDITING_DISTRICTS,

    /**
     * Нет активного состояния диалога
     */
    NONE
}
